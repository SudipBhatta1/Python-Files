# 1. For Loop - Iterating through a range of numbers
print("For Loop Example:")
for i in range(1, 6):  # Prints numbers 1 to 5
    print(f"Number: {i}")

# 2. While Loop - Continues until condition is false
print("\nWhile Loop Example:")
count = 1
while count <= 5:
    print(f"Count: {count}")
    count += 1

# 3. For Loop with List - Iterating through a list
print("\nFor Loop with List Example:")
fruits = ["apple", "banana", "orange"]
for fruit in fruits:
    print(f"Fruit: {fruit}")
